    function sendUserMessage(text) {
      if (!text.trim()) return;
      
      addUserMessage(text);
      
      const typingIndicator = document.createElement('div');
      typingIndicator.className = 'bot-message typing-indicator';
      typingIndicator.id = 'typingIndicator';
      typingIndicator.innerHTML = '<i class="fas fa-circle"></i><i class="fas fa-circle"></i><i class="fas fa-circle"></i>';
      messagesDiv.appendChild(typingIndicator);
      messagesDiv.scrollTop = messagesDiv.scrollHeight;
      
      setTimeout(() => {
        const indicator = document.getElementById('typingIndicator');
        if (indicator) indicator.remove();
        
        const response = findBestResponse(text);
        addBotMessage(response);
      }, 800 + Math.random() * 400);
      
      inputField.value = '';
    }

    sendBtn.addEventListener('click', () => sendUserMessage(inputField.value));
    inputField.addEventListener('keypress', (e) => {
      if (e.key === 'Enter') sendUserMessage(inputField.value);
    });

